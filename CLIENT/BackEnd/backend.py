import json
from os.path import join

# -------------------------------------------------------------------------------------------------
#                                       PARAMETERS
# -------------------------------------------------------------------------------------------------

PARAMETERS_FILE_NAME = join('BackEnd', 'parameters.json')

# -------------------------------------------------------------------------------------------------
#                                       FUNCTION
# -------------------------------------------------------------------------------------------------

def load_parameters() -> None:
    with open(PARAMETERS_FILE_NAME, "r") as archive:
        data = archive.read()
        return json.loads(data)